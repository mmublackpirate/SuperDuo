package it.jaschke.alexandria;

import android.app.Activity;
import android.content.BroadcastReceiver;
import android.content.Context;
import android.content.Intent;
import android.content.IntentFilter;
import android.database.Cursor;
import android.os.Bundle;
import android.support.design.widget.TextInputLayout;
import android.support.v4.app.DialogFragment;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentTransaction;
import android.support.v4.app.LoaderManager;
import android.support.v4.content.CursorLoader;
import android.text.Editable;
import android.text.TextWatcher;
import android.util.Patterns;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.EditText;
import android.widget.ImageView;
import android.widget.TextView;
import com.bumptech.glide.Glide;
import it.jaschke.alexandria.data.AlexandriaContract;
import it.jaschke.alexandria.services.BookService;

public class AddBook extends Fragment implements LoaderManager.LoaderCallbacks<Cursor> {
  private static final String TAG = "INTENT_TO_SCAN_ACTIVITY";
  private static final String PREV_FRAGMENT = "prev_fragment";
  private static final String SCAN_FORMAT = "scanFormat";
  private static final String SCAN_CONTENTS = "scanContents";
  private final int LOADER_ID = 1;
  private final String EAN_CONTENT = "eanContent";
  private EditText ean;
  private View rootView;
  private String mScanFormat = "Format:";
  private String mScanContents = "Contents:";
  private TextInputLayout textInputLayout;
  private BarcodeReceiver barcodeReceiver;

  public AddBook() {
  }

  @Override public void onSaveInstanceState(Bundle outState) {
    super.onSaveInstanceState(outState);
    if (ean != null) {
      outState.putString(EAN_CONTENT, ean.getText().toString());
    }
  }

  @Override public View onCreateView(final LayoutInflater inflater, ViewGroup container,
      Bundle savedInstanceState) {
    setRetainInstance(true);
    rootView = inflater.inflate(R.layout.fragment_add_book, container, false);
    ean = (EditText) rootView.findViewById(R.id.ean);
    ean.addTextChangedListener(new TextWatcher() {
      @Override public void beforeTextChanged(CharSequence s, int start, int count, int after) {
        //no need
      }

      @Override public void onTextChanged(CharSequence s, int start, int before, int count) {
        //no need
      }

      @Override public void afterTextChanged(Editable s) {
        String ean = s.toString();
        //catch isbn10 numbers
        if(ean.length()<13) {

          if (ean.length() == 10 && !ean.startsWith("978")) {
            ean = "978" + ean;
          } else {
            return;
          }
        }


        /**
         * Comment out the following lines so user inputting accidentally will not reset
         * the fields
         */
        //if (ean.length() < 13) {
        //  clearFields();
        //  return;
        //}

        //Once we have an ISBN, start a book intent
        Intent bookIntent = new Intent(getActivity(), BookService.class);
        bookIntent.putExtra(BookService.EAN, ean);
        bookIntent.setAction(BookService.FETCH_BOOK);
        getActivity().startService(bookIntent);
        AddBook.this.restartLoader();
      }
    });

    rootView.findViewById(R.id.scan_button).setOnClickListener(new View.OnClickListener() {
      @Override public void onClick(View v) {
        FragmentTransaction ft = getActivity().getSupportFragmentManager().beginTransaction();
        ScanISBN prev =
            (ScanISBN) getActivity().getSupportFragmentManager().findFragmentByTag(PREV_FRAGMENT);
        if (prev != null) {
          ft.remove(prev);
        }
        ft.addToBackStack(null);
        ScanISBN scanIsbn = new ScanISBN();
        scanIsbn.setStyle(DialogFragment.STYLE_NO_TITLE, R.style.Base_Theme_AppCompat_Dialog_Alert);
        scanIsbn.show(getActivity().getSupportFragmentManager(), PREV_FRAGMENT);
      }
    });

    rootView.findViewById(R.id.save_button).setOnClickListener(new View.OnClickListener() {
      @Override public void onClick(View view) {
        ean.setText("");
        clearFields();
      }
    });

    rootView.findViewById(R.id.delete_button).setOnClickListener(new View.OnClickListener() {
      @Override public void onClick(View view) {
        Intent bookIntent = new Intent(getActivity(), BookService.class);
        bookIntent.putExtra(BookService.EAN, ean.getText().toString());
        bookIntent.setAction(BookService.DELETE_BOOK);
        getActivity().startService(bookIntent);
        ean.setText("");
        clearFields();
      }
    });
    if (savedInstanceState != null && savedInstanceState.getString(EAN_CONTENT) != null) {
      ean.setText(savedInstanceState.getString(EAN_CONTENT));
    }
    return rootView;
  }

  private void restartLoader() {
    getLoaderManager().restartLoader(LOADER_ID, null, this);
  }

  @Override public android.support.v4.content.Loader<Cursor> onCreateLoader(int id, Bundle args) {
    String eanStr = ean.getText().toString();
    if (eanStr.length() == 10 && !eanStr.startsWith("978")) {
      eanStr = "978" + eanStr;
    }
    return new CursorLoader(getActivity(),
        AlexandriaContract.BookEntry.buildFullBookUri(Long.parseLong(eanStr)), null, null, null,
        null);
  }

  @Override
  public void onLoadFinished(android.support.v4.content.Loader<Cursor> loader, Cursor data) {
    if (!data.moveToFirst()) {
      return;
    }

    String bookTitle = data.getString(data.getColumnIndex(AlexandriaContract.BookEntry.TITLE));
    if(bookTitle!=null && bookTitle.length() >0){
    ((TextView) rootView.findViewById(R.id.bookTitle)).setText(bookTitle);
    }

    String bookSubTitle =
        data.getString(data.getColumnIndex(AlexandriaContract.BookEntry.SUBTITLE));
    if(bookSubTitle!=null && bookSubTitle.length() >0) {
      ((TextView) rootView.findViewById(R.id.bookSubTitle)).setText(bookSubTitle);
    }
    String authors = data.getString(data.getColumnIndex(AlexandriaContract.AuthorEntry.AUTHOR));
    if (authors != null && authors.length() > 0) {
      String[] authorsArr = authors.split(",");
      ((TextView) rootView.findViewById(R.id.authors)).setLines(authorsArr.length);
      ((TextView) rootView.findViewById(R.id.authors)).setText(authors.replace(",", "\n"));
    }
    String imgUrl = data.getString(data.getColumnIndex(AlexandriaContract.BookEntry.IMAGE_URL));
    if (imgUrl != null && imgUrl.length() >0) {
      if (Patterns.WEB_URL.matcher(imgUrl).matches()) {
        //Fix glide crashing because of fragment not attaching to activity
        if(getActivity()!=null && !getActivity().isFinishing()) {
          Glide.with(this)
              .load(imgUrl)
              .centerCrop()
              .into((ImageView) rootView.findViewById(R.id.bookCover));
          rootView.findViewById(R.id.bookCover).setVisibility(View.VISIBLE);
        }
      }
    }
    String categories =
        data.getString(data.getColumnIndex(AlexandriaContract.CategoryEntry.CATEGORY));
    if (categories != null && categories.length() > 0) {
      ((TextView) rootView.findViewById(R.id.categories)).setText(categories);
    }
    rootView.findViewById(R.id.save_button).setVisibility(View.VISIBLE);
    rootView.findViewById(R.id.delete_button).setVisibility(View.VISIBLE);
  }

  @Override public void onLoaderReset(android.support.v4.content.Loader<Cursor> loader) {

  }

  private void clearFields() {
    ((TextView) rootView.findViewById(R.id.bookTitle)).setText("");
    ((TextView) rootView.findViewById(R.id.bookSubTitle)).setText("");
    ((TextView) rootView.findViewById(R.id.authors)).setText("");
    rootView.findViewById(R.id.save_button).setVisibility(View.INVISIBLE);
    ((TextView) rootView.findViewById(R.id.categories)).setText("");
    rootView.findViewById(R.id.bookCover).setVisibility(View.INVISIBLE);
    rootView.findViewById(R.id.delete_button).setVisibility(View.INVISIBLE);
  }

  @Override public void onAttach(Activity activity) {
    super.onAttach(activity);
    activity.setTitle(R.string.scan);
  }

  @Override public void onResume() {
    super.onResume();
    if (barcodeReceiver == null) {
      barcodeReceiver = new BarcodeReceiver();
    }
    getActivity().registerReceiver(barcodeReceiver, new IntentFilter(ScanISBN.BARCODE_SCANNED));
  }

  @Override public void onPause() {
    super.onPause();
    getActivity().unregisterReceiver(barcodeReceiver);
  }

  private class BarcodeReceiver extends BroadcastReceiver {

    @Override public void onReceive(Context context, Intent intent) {
      if (intent.getAction().equals(ScanISBN.BARCODE_SCANNED)
          && intent.getStringExtra(ScanISBN.BARCODE_DATA) != null
          && intent.getStringExtra(ScanISBN.BARCODE_DATA).length() > 0) {
        ean.setText(intent.getStringExtra(ScanISBN.BARCODE_DATA));
      }
    }
  }
}

